package com.dji.sample.wayline.model.dto;

import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

/**
 * DJI Cloud API 웨이라인 작업 준비 조건 데이터 전송 객체 (DTO)
 * 
 * 이 클래스는 웨이라인 작업이 실행되기 전에 만족되어야 하는 준비 조건들을 정의하는 데이터 전송 객체입니다.
 * 웨이라인 작업이 안전하고 정상적으로 시작될 수 있도록 필요한 모든 사전 조건들을 포함합니다.
 * 
 * 주요 기능:
 * - 배터리 용량 조건 설정 및 검증
 * - 작업 실행 시간 범위 설정 및 검증
 * - 안전 조건 관리 및 모니터링
 * - 조건 만족 여부 실시간 확인
 * 
 * 조건 검증 과정:
 * 1. 배터리 용량이 최소 요구사항을 만족하는지 확인
 * 2. 현재 시간이 실행 가능 시간 범위 내에 있는지 확인
 * 3. 모든 조건이 만족되면 작업 실행 허용
 * 4. 조건이 만족되지 않으면 작업 실행 지연 또는 취소
 * 
 * 사용 시나리오:
 * - 웨이라인 작업 생성 시 준비 조건 설정
 * - 웨이라인 작업 실행 전 조건 검증
 * - 자동 작업 스케줄링 시 조건 확인
 * 
 * @author sean
 * @version 1.3
 * @date 2023/2/16
 */
@Data
@Builder
@NoArgsConstructor
@AllArgsConstructor
public class WaylineTaskReadyConditionDTO {

    /**
     * 최소 배터리 용량 요구사항
     * 웨이라인 작업 실행을 위해 필요한 최소 배터리 용량 (퍼센트)
     * 
     * 값 범위: 0-100
     * - 0: 배터리 조건 없음
     * - 20: 최소 20% 배터리 필요
     * - 50: 최소 50% 배터리 필요
     * 
     * 안전 고려사항:
     * - 너무 낮은 배터리로 작업 시작 시 비행 중 배터리 부족 위험
     * - 너무 높은 배터리 요구사항 시 작업 실행 기회 제한
     * - 일반적으로 20-30% 정도가 권장됨
     */
    private Integer batteryCapacity;

    /**
     * 작업 실행 시작 가능 시간
     * 웨이라인 작업이 실행될 수 있는 시작 시간 (Unix timestamp)
     * 
     * 값 의미:
     * - 0 또는 null: 시간 제한 없음 (즉시 실행 가능)
     * - 양수 값: 해당 시간 이후부터 실행 가능
     * 
     * 사용 예시:
     * - 야간 작업 제한: 밝은 시간대에만 작업 실행
     * - 예약 작업: 특정 시간에 작업 시작
     * - 기상 조건 고려: 좋은 날씨 시간대에만 작업 실행
     */
    private Long beginTime;

    /**
     * 작업 실행 종료 제한 시간
     * 웨이라인 작업이 실행될 수 있는 종료 시간 (Unix timestamp)
     * 
     * 값 의미:
     * - 0 또는 null: 시간 제한 없음 (무제한 실행 가능)
     * - 양수 값: 해당 시간까지 실행 가능
     * 
     * 사용 예시:
     * - 일몰 전 작업 완료: 어두워지기 전에 작업 완료
     * - 운영 시간 제한: 업무 시간 내에만 작업 실행
     * - 안전 고려: 야간 비행 제한
     */
    private Long endTime;
}
