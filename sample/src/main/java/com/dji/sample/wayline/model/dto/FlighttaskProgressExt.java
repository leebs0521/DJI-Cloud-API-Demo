package com.dji.sample.wayline.model.dto;

import lombok.Data;

/**
 * DJI Cloud API 비행 작업 진행률 확장 정보 데이터 전송 객체 (DTO)
 * 
 * 이 클래스는 웨이라인 작업의 상세한 진행 정보를 포함하는 확장 데이터 전송 객체입니다.
 * 웨이라인 작업 실행 중 현재 웨이포인트, 미디어 생성 상황, 비행 추적 정보 등을 제공합니다.
 * 
 * 주요 기능:
 * - 현재 웨이포인트 인덱스 정보 제공
 * - 미디어 파일 생성 개수 추적
 * - 비행 작업 고유 ID 관리
 * - 비행 경로 추적 ID 관리
 * 
 * 사용 시나리오:
 * - 웨이라인 작업 진행 상황 상세 모니터링
 * - 웨이포인트별 진행률 추적
 * - 미디어 파일 생성 현황 확인
 * - 비행 경로 추적 및 분석
 * 
 * @author sean
 * @version 1.1
 * @date 2022/6/9
 */
@Data
public class FlighttaskProgressExt {

    /**
     * 현재 웨이포인트 인덱스
     * 웨이라인 작업에서 현재 실행 중인 웨이포인트의 인덱스 번호
     * 
     * 값 의미:
     * - 0: 첫 번째 웨이포인트 (시작점)
     * - 양수: 현재 실행 중인 웨이포인트 인덱스
     * - -1: 웨이포인트 없음 또는 오류 상태
     * 
     * 사용 목적:
     * - 웨이라인 작업의 세부 진행 상황 파악
     * - 남은 웨이포인트 개수 계산
     * - 웨이포인트별 소요 시간 분석
     * - 문제 발생 지점 식별
     */
    private Integer currentWaypointIndex;

    /**
     * 현재까지 생성된 미디어 파일 개수
     * 웨이라인 작업 실행 중 현재까지 생성된 미디어 파일(사진, 비디오)의 총 개수
     * 
     * 값 의미:
     * - 0: 아직 미디어 파일이 생성되지 않음
     * - 양수: 생성된 미디어 파일 개수
     * 
     * 미디어 파일 유형:
     * - 사진: 각 웨이포인트에서 촬영된 정지 영상
     * - 비디오: 웨이포인트 간 이동 중 촬영된 동영상
     * - 원시 데이터: 센서 데이터, GPS 로그 등
     * 
     * 사용 목적:
     * - 미디어 파일 생성 진행률 확인
     * - 저장공간 사용량 예측
     * - 업로드 작업 계획 수립
     */
    private Integer mediaCount;

    /**
     * 비행 작업 고유 ID
     * 웨이라인 작업과 연관된 비행 작업의 고유 식별자
     * 
     * ID 특징:
     * - 시스템에서 자동 생성되는 고유 식별자
     * - 웨이라인 작업과 비행 작업을 연결하는 키
     * - 로그 추적 및 디버깅에 사용
     * 
     * 사용 목적:
     * - 비행 작업과 웨이라인 작업의 매핑
     * - 비행 로그 및 이력 추적
     * - 문제 발생 시 원인 분석
     * - 작업 완료 후 결과 검증
     */
    private String flightId;

    /**
     * 비행 경로 추적 ID
     * 웨이라인 작업의 비행 경로를 추적하기 위한 고유 식별자
     * 
     * ID 특징:
     * - 비행 경로의 고유 식별자
     * - GPS 좌표, 고도, 속도 등의 비행 데이터와 연관
     * - 비행 경로 재생 및 분석에 사용
     * 
     * 사용 목적:
     * - 비행 경로 시각화
     * - 비행 데이터 분석
     * - 경로 최적화 연구
     * - 안전성 검증
     */
    private String trackId;
}
