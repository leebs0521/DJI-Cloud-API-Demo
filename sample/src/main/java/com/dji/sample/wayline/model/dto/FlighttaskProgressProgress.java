package com.dji.sample.wayline.model.dto;

import lombok.Data;

/**
 * DJI Cloud API 비행 작업 진행률 정보 데이터 전송 객체 (DTO)
 * 
 * 이 클래스는 웨이라인 작업의 진행 단계와 진행률을 나타내는 정보를 전송하기 위한 데이터 전송 객체입니다.
 * 웨이라인 작업의 전체적인 진행 상황을 단계별로 추적하고 진행률을 제공합니다.
 * 
 * 주요 기능:
 * - 웨이라인 작업의 현재 단계 정보 제공
 * - 웨이라인 작업의 전체 진행률 제공
 * - 작업 진행 상황 실시간 추적
 * - 작업 완료 예상 시간 계산 지원
 * 
 * 사용 시나리오:
 * - 웨이라인 작업 진행률 실시간 모니터링
 * - 작업 단계별 진행 상황 확인
 * - 작업 완료 예상 시간 표시
 * - 작업 진행률 시각화
 * 
 * @author sean
 * @version 1.1
 * @date 2022/6/9
 */
@Data
public class FlighttaskProgressProgress {

    /**
     * 현재 작업 단계
     * 웨이라인 작업에서 현재 실행 중인 작업 단계의 번호
     * 
     * 단계 구성 (일반적인 웨이라인 작업):
     * - 0: 작업 초기화
     * - 1: 드론 이륙 준비
     * - 2: 드론 이륙
     * - 3: 웨이포인트 이동
     * - 4: 미디어 촬영
     * - 5: 드론 착륙
     * - 6: 작업 완료
     * 
     * 값 의미:
     * - 0: 작업 시작 전
     * - 양수: 현재 실행 중인 단계 번호
     * - -1: 오류 상태 또는 단계 정보 없음
     * 
     * 사용 목적:
     * - 작업의 세부 진행 상황 파악
     * - 남은 작업 단계 예측
     * - 단계별 소요 시간 분석
     * - 문제 발생 지점 식별
     */
    private Integer currentStep;

    /**
     * 전체 작업 진행률
     * 웨이라인 작업의 전체 진행률을 나타내는 퍼센트 값
     * 
     * 값 범위: 0-100
     * - 0: 작업 시작 전 (0%)
     * - 1-99: 작업 진행 중 (1-99%)
     * - 100: 작업 완료 (100%)
     * 
     * 진행률 계산 기준:
     * - 웨이포인트 완료 개수 기반
     * - 예상 소요 시간 대비 경과 시간 기반
     * - 작업 단계 완료 개수 기반
     * 
     * 사용 목적:
     * - 작업 완료 예상 시간 계산
     * - 진행률 시각화 (프로그레스 바 등)
     * - 작업 효율성 분석
     * - 사용자에게 진행 상황 알림
     */
    private Integer percent;
}
