package com.dji.sample.manage.model.dto;

import lombok.Data;

/**
 * 디바이스 펌웨어 업그레이드 정보 전송 객체 (DTO) 클래스
 * 
 * DJI Cloud API의 디바이스 펌웨어 업그레이드 정보를 전송하기 위한 데이터 전송 객체입니다.
 * 이 클래스는 다음과 같은 주요 기능들을 제공합니다:
 * 
 * 1. 펌웨어 업그레이드 기본 정보 관리
 *    - 디바이스 이름 및 시리얼 번호 관리
 *    - 제품 버전 정보 관리
 *    - 펌웨어 업그레이드 타입 관리
 * 
 * 2. 디바이스 식별 정보 관리
 *    - 디바이스 시리얼 번호 관리
 *    - 디바이스 모델명 관리
 *    - 디바이스별 업그레이드 정보 추적
 * 
 * 3. 펌웨어 업그레이드 타입 관리
 *    - 펌웨어 업그레이드 타입 분류
 *    - 다양한 업그레이드 방식 지원
 *    - 업그레이드 타입별 처리 로직 구분
 * 
 * 4. API 통신 지원
 *    - Lombok 어노테이션을 통한 자동 getter/setter 생성
 *    - API 요청/응답에서 사용되는 표준화된 형식 제공
 *    - 펌웨어 업그레이드 요청의 표준화된 데이터 구조 제공
 * 
 * 이 클래스는 디바이스 펌웨어 업그레이드 과정에서 필요한 모든 정보를
 * 포함하며, 펌웨어 업그레이드 요청을 처리하는 표준화된
 * 데이터 형식을 제공합니다.
 * 
 * @author sean
 * @version 1.2
 * @date 2022/8/16
 */
@Data
public class DeviceFirmwareUpgradeDTO {

    /**
     * 디바이스 이름
     * 펌웨어를 업그레이드할 디바이스의 모델명
     */
    private String deviceName;

    /**
     * 디바이스 시리얼 번호
     * 펌웨어를 업그레이드할 디바이스의 시리얼 번호
     */
    private String sn;

    /**
     * 제품 버전
     * 업그레이드할 펌웨어의 제품 버전 정보
     */
    private String productVersion;

    /**
     * 펌웨어 업그레이드 타입
     * 펌웨어 업그레이드의 타입 (예: 0-자동, 1-수동, 2-강제 등)
     */
    private Integer firmwareUpgradeType;
}
